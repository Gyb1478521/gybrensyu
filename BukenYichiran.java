    public void getlistInfo() {

        BukenService list = new BukenService();
        List<BukenBean> listinfo = list.listInfo();
        DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
        df.setRowCount(0);
        for (BukenBean c : listinfo) {
            df.addRow(new Object[]{c.getid(), c.getBuken_name(), c.getAddress(), c.getEki(), c.getGrade(), c.getCreate_date(), c.getModify_date()});
        }
    }

