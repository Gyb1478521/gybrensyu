        int row = jTable1.getSelectedRow();
        BukenBean bb = new BukenBean();
        bb.setid(df.getValueAt(row, 0).toString());
        bb.setBuken_name(df.getValueAt(row, 1).toString());
        bb.setAddress(df.getValueAt(row, 2).toString());
        bb.setEki(df.getValueAt(row, 3).toString());
        bb.setGrade(df.getValueAt(row, 4).toString());

